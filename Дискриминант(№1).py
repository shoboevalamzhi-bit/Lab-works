def f(a, b, c):
    d = b**2 - 4 * a * c
    
    if d > 0:
        x1 = (-b + d**0.5) / (2 * a)
        x2 = (-b - d**0.5) / (2 * a)
        print(f"Дискриминант = {d} > 0 → два различных корня")
        print(f"Корни уравнения: x₁ = {x1:.2f}, x₂ = {x2:.2f}")
        
        print(f"\nПроверка по теореме Виета:")
        print(f"x1 + x2 = {x1 + x2:.2f}")
        print(f"x1 * x2 = {x1 * x2:.2f}")
        
        if abs((x1 + x2) - (-b/a)) < 1e-10 and abs((x1 * x2) - (c/a)) < 1e-10:
            print("Проверка пройдена: Теорема Виета выполняется")
        else:
            print("Проверка не пройдена")
            
    elif d == 0:
        x = -b / (2 * a)
        print(f"Дискриминант = 0 → один корень (два одинаковых)")
        print(f"Корень уравнения: x₁ = x₂ = {x:.2f}")
        
        print(f"\nПроверка по теореме Виета:")
        print(f"x₁ + x₂ = {2*x:.2f} (должно быть -b/a = {-b/a:.2f})")
        print(f"x₁ * x₂ = {x*x:.2f} (должно быть c/a = {c/a:.2f})")
        
        if abs((2*x) - (-b/a)) < 1e-10 and abs((x*x) - (c/a)) < 1e-10:
            print("Проверка пройдена: Теорема Виета выполняется")
        else:
            print("Проверка не пройдена")
            
    else:
        print(f"Дискриминант = {d} < 0 → нет действительных корней")
        return "Нет действительных корней"
    
    return x1, x2 if d > 0 else (x, x)

print("\nВведите параметры квадратного уравнения (ax² + bx + c = 0):")
a = float(input("a = "))
b = float(input("b = "))
c = float(input("c = "))

f(a, b, c)
